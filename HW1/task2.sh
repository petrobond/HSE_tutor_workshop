dir="${1:-}"
if [[ -z "$dir" ]]; then
  echo "Использование: $0 <директория>" >&2
  exit 1
fi

echo "PATH (до):"
printf '%s\n' "$PATH"

# Добавляем, если ещё нет
case ":$PATH:" in
  *":$dir:"*) echo "Директория уже в PATH: $dir" ;;
  *) export PATH="$PATH:$dir"; echo "Добавлено: $dir" ;;
esac

echo "PATH (после):"
printf '%s\n' "$PATH"

# Изменения переменных окружения, сделанные в текущем терминале (export PATH=...), живут только в этой сессии. 
# При закрытии окна оболочки процесс завершается, и новое окружение не сохраняется.
