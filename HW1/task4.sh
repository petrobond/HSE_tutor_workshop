greet() { printf 'Hello, %s\n' "${1:-}"; }
add()   { echo "$(( ${1:-0} + ${2:-0} ))"; }

name="${1:-World}"
greet "$name"

read -r -p "Введите первое число: " a
[[ "$a" =~ ^-?[0-9]+$ ]] || { echo "Ошибка: введите целое число."; exit 1; }

read -r -p "Введите второе число: " b
[[ "$b" =~ ^-?[0-9]+$ ]] || { echo "Ошибка: введите целое число."; exit 1; }

sum="$(add "$a" "$b")"
echo "Сумма $a и $b равна $sum"
